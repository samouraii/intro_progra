#!/usr/bin/python

nb_user = int(input("Entrez un nombre de 1 Ã  7 : "))

if nb_user == 1:
    print("Lundi")

elif nb_user == 2:
    print("Mardi")

elif nb_user == 3:
    print("Mercredi")

elif nb_user == 4:
    print("Jeudi")

elif nb_user == 5:
    print("Vendredi")

elif nb_user == 6:
    print("Samedi")

elif nb_user == 7:
    print("Dimanche")

else:
    print("Erreur : {} n'est pas compris entre 1 et 7 !".format(nb_user))










